    private GeobaseRegion geobaseRegion;

    Region(int id) {
        this.geobaseRegion = geobaseRegion(id);
    }

    public static GeobaseRegion getCapital(int regionId) {
        return getCapital(geobaseRegion(regionId));
    }

    public static GeobaseRegion getCapital(GeobaseRegion region) {
        if (region == null) {
            throw new IllegalArgumentException("Argument \"region\" must not be null");
        }
        if (region.getCapitalId() == 0) {
            throw new IllegalArgumentException("Region " + region.getEnName() + " has no capital id");
        }
        return geobaseRegion(region.getCapitalId());
    }

    public static LocalDateTime getTime(int regionId) {
        return getTime(geobaseRegion(regionId));
    }

    public static LocalDateTime getTime(GeobaseRegion region) {
        if (region == null) {
            throw new IllegalArgumentException("Argument \"region\" must not be null");
        }
        if (region.getTzname() == null || region.getTzname().isEmpty()) {
            throw new IllegalArgumentException("No tzName found for region " +
                    region.getEnName() + " [" + region.getId() + "]");
        }
        return LocalDateTime.now(forTimeZone(getTimeZone(region.getTzname())));
    }

    public GeobaseRegion getGeobaseRegion() {
        return geobaseRegion;
    }

    public int getId() {
        return geobaseRegion.getId();
    }

    public GeobaseRegion getCapital() {
        return getCapital(geobaseRegion);
    }

    public LocalDateTime getTime() {
        return getTime(geobaseRegion);
    }
