async def test_autogenerated_heterogeneous(taxi_userver_sample):
    response = await taxi_userver_sample.get('autogen/heterogeneous-responses')
    assert response.status_code == 200
    assert response.json() == ['It', 'is', 'fine']


async def test_autogenerated_heterogeneous_strings(taxi_userver_sample):
    test_data = 'Some test data'
    response = await taxi_userver_sample.post(
        'autogen/heterogeneous-responses',
        data=test_data,
        headers={'Content-Type': 'application/octet-stream'},
    )
    assert response.status_code == 200
    assert response.text == test_data
