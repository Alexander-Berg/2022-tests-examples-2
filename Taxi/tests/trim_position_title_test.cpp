#include <gtest/gtest.h>

#include <payture/payture_receipt_parser.hpp>

TEST(TrimPositionTitle, Short) {
  const std::string title = payture::TrimPositionTitle("short");

  ASSERT_EQ(title, "short");
}

TEST(TrimPositionTitle, Long) {
  const std::string title = payture::TrimPositionTitle(
      "123456789012345678901234567890123456789012345678901234567890123456789012"
      "3456789012345678901234567890123456789012345678901234567890");

  ASSERT_EQ(title,
            "123456789012345678901234567890123456789012345678901234567890123456"
            "78901234567890123456789012345678901234567890123456789012345...");
}

TEST(TrimPositionTitle, Exactly) {
  const std::string title = payture::TrimPositionTitle(
      "123456789012345678901234567890123456789012345678901234567890123456789012"
      "34567890123456789012345678901234567890123456789012345678");

  ASSERT_EQ(title,
            "123456789012345678901234567890123456789012345678901234567890123456"
            "78901234567890123456789012345678901234567890123456789012345678");
}
